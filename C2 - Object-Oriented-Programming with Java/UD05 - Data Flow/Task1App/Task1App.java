package Task1App;

public class Task1App {

	public static void main(String[] args) {
		// Two integers
		int m_first_var = 30, m_second_var = 30;
		
		// Simple 3 condition if-else conditional block
		if (m_first_var == m_second_var)
		{
			System.out.println(m_first_var + " == " + m_second_var);
		}
		else
		if (m_first_var > m_second_var)
		{
			System.out.println(m_first_var + " > " + m_second_var);
		}
		else
		{
			System.out.println(m_first_var + " < " + m_second_var);
		}

	}

}
