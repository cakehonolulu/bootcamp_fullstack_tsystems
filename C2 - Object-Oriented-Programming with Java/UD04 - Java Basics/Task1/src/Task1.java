
public class Task1 {

	public static void main(String[] args) {
		int m_num_a = 20;
		int m_num_b = 5;
		
		System.out.println(m_num_a + " + " + m_num_b + " = " + (m_num_a + m_num_b));
		System.out.println(m_num_a + " - " + m_num_b + " = " + (m_num_a - m_num_b));
		System.out.println(m_num_a + " * " + m_num_b + " = " + (m_num_a * m_num_b));
		System.out.println(m_num_a + " / " + m_num_b + " = " + (m_num_a / m_num_b));
		System.out.println(m_num_a + " % " + m_num_b + " = " + (m_num_a % m_num_b));
	}

}
